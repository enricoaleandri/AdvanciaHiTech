                            <h3>Offerta Formativa</h3>					Per l'esperienza accumulata in questi anni, 
                            <font color="#C00000">Advancia Technology</font> è oggi in grado di offrire ai propri clienti l'indispensabile e necessaria formazione nei settori informatici di sua competenza.					Per 
                            <font color="#C00000">Advancia Technology</font>, la formazione è intesa come strumento per facilitare lo sviluppo e la valorizzazione delle competenze degli allievi, per migliorare e facilitare la loro integrazione con le nuove tecnologie, per riqualificare e/o aggiornare gli skill professionali di personale del cliente.
                            <br />					Per questi motivi, 
                            <font color="#C00000">Advancia Technology</font> propone l'erogazione di servizi per la formazione suddivisi in tre principali categorie:					
                            <ul>
                                <li>Corsi di formazione personalizzati</li>
                                <li>Affiancamento e consulenza formativa programmata</li>
                                <li>Corsi di formazione standard</li>
                            </ul>					Per tutte e tre le categorie, 
                            <font color="#C00000">Advancia Technology</font> propone l'erogazione del servizio sia presso la sede del cliente che presso la propria sede operativa.
                            <br />
                            <br />
                            <strong>Corsi di formazione personalizzati</strong>
                            <br />
                            <img src="css/images/corsi_affiancamento.jpg" alt="" class="right" />
                            <font color="#C00000">Advancia Technology</font> offre la possibilità di progettare un corso personalizzato secondo le esigenze specifiche del cliente. Il cliente verrà affiancato da un nostro docente specialista e il corso verrà così strutturato nei tempi e nei contenuti, in funzione delle particolari richieste. Il corso viene di norma articolato in sessioni teoriche e sessioni di esercitazioni pratiche distribuite su più giornate. A seconda della quantità delle giornate preventivate, i corsi potranno essere anche suddivisi in settimane non consecutive distribuite nell'anno. Su specifiche richieste del cliente, possono venire inseriti nel programma del corso anche momenti di verifica e test del livello di apprendimento raggiunto dai singoli partecipanti. 	Dal punto di vista della qualità, al termine del corso, a ciascun partecipante verrà distribuito un modulo nel quale potrà essere liberamente espresso il giudizio del corso sia dal punto di vista dei contenuti che dal punto di vista della qualità della docenza erogata. I corsi di formazione personalizzati si adattano anche a quelle realtà in cui il cliente desidera pianificare la formazione del proprio personale nel corso dell'anno sia dal punto di vista della preparazione di base che dal punto di vista dell'aggiornamento formativo.
                            <br />
                            <br />
                            <strong>Affiancamento e consulenza formativa programmata</strong>
                            <br />
                            <img src="css/images/corsi_personalizzati.jpg" alt="" class="left" />
                            <font color="#C00000">Advancia Technology</font> offre la possibilità di poter erogare ad uno o più allievi indicati dal cliente, un servizio di formazione dedicata per uno specifico progetto e/o prodotto. Tale servizio può essere erogato: presso la sede del cliente, presso la sede 
                            <font color="#C00000">Advancia Technology</font> o anche telefonicamente. Il servizio può essere erogato per un numero prestabilito di ore e/o giorni opportunamente programmati in periodi di tempo (un mese, sei mesi, un anno) prefissati. Nel tempi previsti e concordati con il cliente, lo specialista	
                            <font color="#C00000">Advancia Technology</font> nella sua veste di tutor, seguirà l'allievo o gli allievi indicato/i dal cliente erogando la propria consulenza formativa. Tale servizio di affiancamento e consulenza formativa si adatta nei casi in cui il cliente deve seguire, con il proprio personale progetti che prevedono l'utilizzo di strumenti software di recente acquisizione e/o non ancora noti ai tecnici incaricati del relativo utilizzo. Un altro caso in cui tale servizio si rende utile è quello in cui il cliente intende far svolgere degli stage al proprio personale e/o collaboratori, e desidera far affiancare tali allievi da un tutor esterno.
                            <br />
                            <br />
                            <strong>Corsi di formazione standard</strong>
                            <br />
                            <img src="css/images/corsi_standard.jpg" alt="" class="right" />					In ognuno degli ambiti previsti, 
                            <font color="#C00000">Advancia Technology</font> ha introdotto nella propria offerta uno o più percorsi formativi all'interno dei quali sono inclusi i corsi di formazione specifici. In generale ciascun corso viene erogato entro i 5gg della settimana.					Il cliente ha la possibilità:					
                            <ul>
                                <li>di iscrivere i partecipanti direttamente ai corsi prescelti previsti a calendario tenuti presso la sede 
                                    <font color="#C00000">Advancia Technology</font>; l'iscrizione, in questo caso, verrà confermata al cliente solo nel caso di raggiungimento del numero minimo di iscritti (normalmente 4)
                                </li>
                                <li>di richiedere l'erogazione del/i corso/i presso la propria sede; in questo caso sia il costo del corso/i che il relativo contenuto/programma, potranno essere concordati</li>
                            </ul>					Nel seguito verranno elencati i corsi standard previsti, raggruppati per ambito e per percorso formativo. Per la descrizione dettagliata del corso, il relativo programma e contenuto, si rimanda alla consultazione della specifica scheda. Per alcuni corsi, nella relativa scheda, sono indicati i corsi propedeutici consigliati che, per un corretto apprendimento, l'allievo dovrebbe aver già seguito.
